
package cn.jingling.lib.file;

public class OtherException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 5509699340596599704L;

    public OtherException() {
        super();
    }

    public OtherException(String msg) {
        super(msg);
    }

    public OtherException(Throwable cause) {
        super(cause);
    }

    public OtherException(String msg, Throwable cause) {
        super(msg, cause);
    }
}
